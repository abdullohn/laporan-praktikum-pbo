        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();

        Anggota1841720209Abdulloh ang = new Anggota1841720209Abdulloh().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        ang.delete();
