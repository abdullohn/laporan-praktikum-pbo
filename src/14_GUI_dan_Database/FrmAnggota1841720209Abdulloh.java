        int row = tblAnggota.getSelectedRow();
        
        Anggota1841720209Abdulloh agt = new Anggota1841720209Abdulloh().getByIdAbdulloh(Integer.parseInt(model.getValueAt(row, 0).toString()));
        agt.deleteAbdulloh();
