    public void tampilkanDataAbdulloh(){
        String[] kolom = {"ID", "Nama", "Keterangan", "Telepon"};
        ArrayList<Anggota1841720209Abdulloh> list = new Anggota1841720209Abdulloh().getAllAbdulloh();
        Object rowData[] = new Object[4];
        
        tblAnggota.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for(Anggota1841720209Abdulloh agt : list){
            rowData[0] = agt.getmIdAnggotaAbdulloh();
            rowData[1] = agt.getmNamaAbdulloh();
            rowData[2] = agt.getmAlamatAbdulloh();
            rowData[3] = agt.getmTeleponAbdulloh();
            
            ((DefaultTableModel)tblAnggota.getModel()).addRow(rowData);
        }
    }
    
    public void cariAbdulloh(String keyword){
        String[] kolom = {"ID", "Nama", "Keterangan"};
        ArrayList<Anggota1841720209Abdulloh> list = new Anggota1841720209Abdulloh().search(keyword);
        Object rowData[] = new Object[4];
        
        tblAnggota.setModel(new DefaultTableModel(new Object[][]{}, kolom));
        
        for(Anggota1841720209Abdulloh agt : list){
            rowData[0] = agt.getmIdAnggotaAbdulloh();
            rowData[1] = agt.getmNamaAbdulloh();
            rowData[2] = agt.getmAlamatAbdulloh();
            rowData[3] = agt.getmTeleponAbdulloh();
            
            ((DefaultTableModel)tblAnggota.getModel()).addRow(rowData);
        }
    }
    
